package stringMethods;
//Java String trim() method
public class Stringtrim {

	public static void main(String[] args) {
		String s = "    Sachin      ";
		System.out.println(s);//Sachin Without trim
        System.out.println(s.trim());//Sachin with trim
        
        String s1 = "Sachin Tendulkar";
        System.out.println(s1.trim());//Sachin Tendulkar

	}

}
