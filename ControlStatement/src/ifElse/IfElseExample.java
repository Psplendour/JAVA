package ifElse;
//if - else
public class IfElseExample 
{

	public static void main(String[] args) 
	{
		int age=10;  
	    //checking the age  
	    if(age>18)
	    {  
	        System.out.print("Age is greater than 18");  
	    } 
	    else 
	    {
	    	System.out.print("Age is less than 18");
	    }

	}

}
